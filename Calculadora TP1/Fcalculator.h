#include <stdio.h>

/** \brief La función suma los dos operadores ingresados por el usuario
 *
 * \param   Primer operando ingresado
 * \param segundo operando ingresado
 * \return retorna el resultado de la suma
 *
 */
float sumaDeOperandos(float, float);

/** \brief La funcion resta los dos operadores ingresados por el usuario
 *
 * \param float primer operando ingresado
 * \param float segundo operando ingresado
 * \return Retorna el resultado de la resta
 *
 */
float restaDeOperados(float, float);

/** \brief La funcion realiza la division de los dos operadores ingresados por el usuario
 * pero si algun operador es 0, el usuario recibe el mensaje de
 * "No es posible dividir por 0"
 * \param int primer operando
 * \param float segundo operando
 * \return Retorna el resultado de la division
 *
 */
float divisionDeOperandos(int, float);

/** \brief La funcion realiza la multiplicacion de los dos operadores ingresados por el usuario
 *
 * \param int primer operando
 * \param int   segundo operando
 * \return Retorna el resultado de la multiplicacion
 *
 */
int multiplicacionDeOperandos(int, int);

/** \brief La funcion realiza el factorial de un numero ingresado por el usuario
 *
 * \param int
 * \return retorna el resultado del factorial
 *
 */
int factorialDelNumero(int);
